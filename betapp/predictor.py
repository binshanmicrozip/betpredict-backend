# PATTERN LIBRARY — loaded from betapp/patterns_library.json
# Run update_patterns.py to regenerate with new data
# ══════════════════════════════════════════════════════════════

def _load_pattern_library() -> dict:
    """
    Load pattern library from JSON file.
    Primary location: betapp/patterns_library.json (relative to this file)
    Falls back to additional search paths if primary not found.
    JSON is generated by update_patterns.py from features_dataset.csv
    """
    search_paths = [
        os.path.join(BASE_DIR, "betapp", "patterns_library.json"),   # PRIMARY
        os.path.join(BASE_DIR, "patterns_library.json"),
        os.path.join(BASE_DIR, "data", "patterns_library.json"),
        os.path.join(BASE_DIR, "..", "data", "patterns_library.json"),
        os.path.join(BASE_DIR, "..", "data", "parsed", "patterns_library.json"),
    ]

    for path in search_paths:
        resolved = os.path.abspath(path)
        if os.path.exists(resolved):
            try:
                with open(resolved, "r") as f:
                    data = json.load(f)
                lib = data.get("library", data)
                total_patterns = sum(
                    len(c.get("patterns", {})) for c in lib.values()
                    if isinstance(c, dict)
                )
                print(f"[PatternLibrary] Loaded from {resolved} — {total_patterns} patterns")
                return lib
            except (json.JSONDecodeError, Exception) as e:
                print(f"[PatternLibrary] ERROR reading {resolved}: {e}")

    print(
        "[PatternLibrary] WARNING: patterns_library.json not found in any search path.\n"
        "  Searched:\n" + "\n".join(f"    - {os.path.abspath(p)}" for p in search_paths) +
        "\n  Run update_patterns.py to generate the file."
    )
    return {}


PATTERN_LIBRARY = _load_pattern_library()


